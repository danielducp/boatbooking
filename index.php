<h4>What boats are available to sail in? <span id="SpanHeader4" >    <i id="icon4" class="fa fa-angle-down"></i>
        </span> </h4></a>
<div id="q4" class="answertext" style="display:none;">
<p>We have three types of boats which have been selected to enable sailors of all abilities to enjoy their time on the water:-<br>

1. Our largest fleet of boats are Challenger Trimarans. These boats are very stable and comfortable to sail. We help our sailors into the cockpit of the boat on the shore. Our volunteers will help carers with this and we have two Invacare Jasmine battery operated passive sling lifts fitted with lifting attachments to suit both type of commonly used slings. The Buddy then sits alongside the sailor to provide as much help and reassurance as is required.<br>

2. Access Dinghies are designed for sailors who would love to have a go at sailing, but find it too difficult or uninviting to do so. Whether as a recreation, a competition sport or a therapeutic activity, the Access Dinghy design features allow total novices of any ability to sail with little tuition. Our team of instructors will be available for learning to sale.<br>

3. The Deben Lugger is an 18’ traditionally styled day boat with lug rig designed and built using up to date technology and materials. Its design makes it a very safe and stable boat This boat can hold up to six people and allows us to take small groups of sailors with their carers. Hoists are also available to transfer sailors safely from the pontoon into the boat and out <br></div>

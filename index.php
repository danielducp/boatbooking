Sessions are open to anyone with a disability both young and old. Most of our sailors come from the Peterborough area but quite a number travel from much further afield to enjoy the facilities we can offer.Aged from 8 to 80+ and with a huge range of disabilities our sailors can experience sailing in any of our three types of boats and take part to the level that they enjoy and are comfortable with. Our sailors range from independent individuals to large groups from a range of care establishments. All are welcome.<br>
